#include "libft.h"

void	ft_putnstr_fd(char *s, int fd, int n)
{
	int	i;

	if (s == NULL)
		return ;
	i = 0;
	while ((s[i] != '\0') && (i < n))
	{
		write(fd, &s[i], 1);
		i++;
	}
}
